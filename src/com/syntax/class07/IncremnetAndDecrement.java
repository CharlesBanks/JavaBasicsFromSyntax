package com.syntax.class07;

public class IncremnetAndDecrement {

	public static void main(String[] args) {
		
		int x=10;
		
		x=x+1;
		x+=1;
		
		System.out.println(x);

	
	x++; //increments value of a variable by 1 
	System.out.println(x);
	
	x--;//decrements value of a variable by 1 
	System.out.println(x);
	
	//increments and decrements operators work only with variable
	
	//10++; CE invalid arguments to operation ++/--
	
	int num = 100;
	num++;
	System.out.println(num);
	
	
	}

}
